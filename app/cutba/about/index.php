    <div class="heading">
        <h4>History of CUTBA</h4>
    </div>

    <div class="timeline">
        <div class="timelineItem">
            <div class="date">
                <h7>April 22</h7>
                <h6>1977</h6>
            </div>
            <div class="timelineText">
                <p>The first meeting of former Tiger Band Alumni took place at the Clemson House. The attendees were Larry Sloan ‘74, Keith Snelgrove '77, Tony Stapleton ’76, Jeff Tisdale ‘65, and Frank Johnson ‘66.</p>
                <p>This impromptu meeting was followed up the next day with an official meeting at the Holiday Inn in Clemson to initiate this organization and to begin the structure and define the purpose of the new group.</p>
                <p>In addition to the attendees of the previous day, it was attended by Dr. Bruce Cook, director of Tiger Band and Mr. Nick Peck, voice of Tiger Band.</p>
		<p>Larry Sloan was chosen as the first President of CUTBA, Frank Johnson was chosen as the first Vice President, and Keith Snelgrove was chosen as the first Secretary.</p>
            </div>
        </div>
        <div class="timelineItem">
            <div class="date">
                <h7>November 7</h7>
                <h6>1977</h6>
            </div>
            <div class="timelineText">
                <p>The first CUTBA Newsletter was distributed. A Board of Directors was established consisting of ten CUTBA members in good standing plus one representative from Tiger Band plus other exofficio Board members (including the Director of Tiger Band) to be selected by the CUTBA Board of Directors. Initially the term of office for each Board Member was indefinite, but later the terms were changed to be three year rotating terms for active Board Members, one year for the Tiger Band representative and indefinite for the ex-officio members.</p>
                <p>A Constitution was written followed by a set of By-Laws.</p>
            </div>
        </div>
        <div class="timelineItem">
            <div class="date">
                <h7></h7>
                <h6>1977-1978</h6>
            </div>
            <div class="timelineText">
                <p>Joe Turner of the Alumni Association was instrumental in helping establish CUTBA’s goals and development during the early years. One of the first successful major projects undertaken by CUTBA was lobbying to the University administration and to IPTAY to furnish new Tiger Band uniforms.</p>
            </div>
        </div>
        <div class="timelineItem">
            <div class="date">
                <h7>July 5</h7>
                <h6>1978</h6>
            </div>
            <div class="timelineText">
                <p>CUTBA was first recognized by Clemson University as an official University organization in a letter from George Moore, Director of Alumni Relations to Larry Sloan.</p>
            </div>
        </div>
        <div class="timelineItem">
            <div class="date">
                <h7>June 30</h7>
                <h6>1982</h6>
            </div>
            <div class="timelineText">
                <p>An agreement was signed between CUTBA and Clemson University to award the “Clemson University Tiger Band Association Award” annually to the Tiger Band Commander annually on Honors and Awards Day.</p>
                <p>This was a $200.00 cash award however it required an initial gift of $2,500.00 for the endowment of the award. At that time CUTBA only had $834.00 to donate so it was a couple more years before the award actually began.</p>
                <p>The first partial scholarship was awarded to a Tiger Band student in 1988.</p>
            </div>
        </div>
        <div class="timelineItem">
            <div class="date">
                <h7></h7>
                <h6>1983</h6>
            </div>
            <div class="timelineText">
                <p>CUTBA sponsored the first Alumni Band at Homecoming. There were approximately 60 participants in the initial Alumni Band.</p>
                <p>To date the Alumni Band has continued to thrive and grow each year.</p>
            </div>
        </div>
        <div class="timelineItem">
            <div class="date">
                <h7></h7>
                <h6>2006</h6>
            </div>
            <div class="timelineText">
                <p>CUTBA established the “Tiger Band Commitment” project. This was a goal to raise $2 million in two years for the scholarship endowment. This successful project actually raised approximately $2,300,000 in cash, pledges and bequeaths.</p>
            </div>
        </div>
        <div class="timelineItem">
            <div class="date">
                <h7></h7>
                <h6>2007</h6>
            </div>
            <div class="timelineText">
                <p>CUTBA established a “sales website” in conjunction with a host firm in Greenville, SC for the purpose of raising additional scholarship money.</p>
            </div>
        </div>
       	    <div class="timelineItem">
            <div class="date">
                <h7></h7>
                <h6>2008</h6>
            </div>
            <div class="timelineText">
                <p>CUTBA established the “Continue the Commitment” project. This project is currently ongoing.</p>
            </div>
        </div>

	 <div class="timelineItem">
            <div class="date">
                <h7></h7>
                <h6>2009</h6>
            </div>
            <div class="timelineText">
                <p>The CUTBA Alumni Band first marched in the “First Friday Parade” which has become an annual event.</p>
                <p>CUTBA established ten “Regions” around the southeast from Atlanta to Washington DC in order to allow more personal involvement from each CUTBA member and to continue the growth of CUTBA.</p>
                <p>The original Regions and Regional Representatives consist of the following: Atlanta – Chuck Allen, Charleston- Ben Adams, Charlotte – Kevin Wigington, Columbia – Stephanie Stanley, Greensboro – John Smith, Greenville – Calvin Pennington, Spartanburg – Molly Schaefer, Tri-County (Anderson/Oconee/Pickens) – Allison Wells, Washington DC – Dale Walsh</p>
            </div>
        </div>
        <div class="timelineItem">
            <div class="date">
                <h7></h7>
                <h6>2010</h6>
            </div>
            <div class="timelineText">
                <p>CUTBA Alumni Band first performed at the annual spring football game.</p>
                <p> CUTBA Alumni Band performed at selected Clemson basketball and baseball games.</p>
                <p>CUTBA Alumni Band first performed in the IPTAY spring Prowl &amp; Growl events. This has become an annual event.</p>
                <p>CUTBA set up a sales booth at the annual Clemson Reunion. The purpose was to promote Tiger Band and to raise money for scholarships.</p>
            </div>
        </div>
	 <div class="timelineItem">
            <div class="date">
                <h7></h7>
                <h6>2012</h6>
            </div>
            <div class="timelineText">
                <p>The John H. Butler / Bruce F. Cook Tiger Band Plaza project was announced and fundraising was initiated.</p>
            </div>
        </div>

        <div class="timelineItem">
            <div class="date">
                <h7></h7>
                <h6>2013</h6>
            </div>
            <div class="timelineText">
                <p>The Alumni Band celebrated its 30’th year of marching at the Homecoming halftime with a “300 for 30” campaign to march 300 Alumni Band members. In actuality, 382 Alumni Band members marched at halftime.</p>
            </div>
        </div>
            <div class="timelineItem">
            <div class="date">
                <h7></h7>
                <h6>2014</h6>
            </div>
            <div class="timelineText">
                <p>On May 23, construction began on phase one of the John H. Butler / Bruce F. Cook Tiger Band Plaza. It was dedicated in October.</p>
		<p>CUTBA established the CUTBA Student Advisory Board.  Its founding members consisted of: Teague Albenesius, John Bismack, Kimberly Breland, Mitchel Colebank, Megan Earnest, Mindy Earnest, Katie Ferguson, Scot Gilbert, Dakota Gray, Blake Jenkins, Jared Kleven, Stephen McAfee, Traivon McNair, Dean Millare, Michael Poda, Gregg Prange, Emily Schaub, Lowndes Taylor, and Maria Zazzara.</p>
            </div>
        </div>
      <div class="timelineItem">
            <div class="date">
                <h7></h7>
                <h6>2015</h6>
            </div>
            <div class="timelineText">
                <p>Two additional active board members were added to the Board of Directors making a total of 12 active board members plus one active student board member from Tiger Band. In addition, another active board member was added from the Student Advisory Board.</p>
            </div>
        </div>
        <div class="timelineItem">
            <div class="date">
                <h7></h7>
                <h6>2016</h6>
            </div>
            <div class="timelineText">
                <p>With help from the Alumni Association, CUTBA intiated the move to became a 501c-3, non-profit organization which will take effect on July 1, 2017. CUTBA still falls under the umbrella of the Alumni Association, but operates as a self-entity.</p>
		<p>The CUTBA scholarship endowment surpassed $1,000,000 in cash.</p>
		<p>The Spirit Tiger was completed as a second phase of the John H. Butler / Bruce F. Cook Tiger Band Plaza "Continue the Commitment" project.</p>
            </div>
        </div>
    </div>

